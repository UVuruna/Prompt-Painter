"""PromptPainter configuration — every tunable value lives here.

Selectors rot with every site reskin: each DOM hook below is a tuple
of fallbacks tried in order, and when none match the driver FAILS
LOUDLY (root Rule #1) instead of guessing.

This package is split by domain (paths, formatters, sheet contract,
postprocess, upscale, aspect + shared filter framework, theme, jobs,
jobtemp, ai, sites) — this module re-exports the FULL public API so
every existing ``config.X`` / ``from painter.config import X`` call
site keeps working unchanged. It IS the package's public interface,
not a temporary bridge (root Rule #6 does not apply to this
re-export — see the refactor plan's config section).
"""

